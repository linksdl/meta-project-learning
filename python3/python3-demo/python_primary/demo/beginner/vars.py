# python 中在程序运行时可以随着程序的运行更改的量成为变量，比如我们可以定义一个变量i,并将数字5赋给变量。
# 变量有个特点，就是即使赋值，也可以更改。

# 赋值
i = 7
print(i)

# 变量的应用
i = 5
print (i)
i = 6
print(i)
i = 7
print(i)

i += 3
print(i)























